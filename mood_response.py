
def mood_responses(mood):
    if mood == "sad":
        print("I'm sorry you feel sad, maybe an ice cream will make you feel better.")
    elif mood == "happy":
        print("That's great you are feeling happy, you must be having a great day!")
    elif mood == "excited":
        print("I love feeling excited!")
    else:
        print("Invalid input, please respond with one of the following option: Happy/Sad/Excited.")